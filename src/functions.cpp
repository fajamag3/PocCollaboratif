#include "functions.h"


namespace OPERATIONS
{
	// ==============================
	// === Addition (Amadou) ========
	// ==============================
	long double addition(long double a, long double b)
	{
    		return a + b;
	}

	// ==============================
	// === Multiplication (Faris) ===
	// ==============================
	long double multiplication(int a, int b)
	{
    		return a * b;
	}

	// ==============================
	// === Subtraction (Jacky) =====
	// ==============================

	long double subtraction(long double num1, long double num2)
	{

    		return num1 - num2;
	}

}
